package com.example.vendeton.Entidades;

public class CorreoElectronico {

    public String cor_usuario, cor_dominio, cor_correo;
    public int cor_id, con_identificacion;

    public CorreoElectronico(String cor_usuario, String cor_dominio, String cor_correo, int cor_id, int con_identificacion) {
        this.cor_usuario = cor_usuario;
        this.cor_dominio = cor_dominio;
        this.cor_correo = cor_correo;
        this.cor_id = cor_id;
        this.con_identificacion = con_identificacion;
    }


}
